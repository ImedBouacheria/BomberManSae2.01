package com.example.bombermansae201;

/**
 * Énumération représentant les différents types de power-ups
 * disponibles dans le jeu
 */
public enum PowerUpType {
    BOMB_COUNT, // Augmente le nombre de bombes
    BOMB_POWER, // Augmente la puissance des bombes
    SPEED,      // Augmente la vitesse de déplacement
    LIFE        // Ajoute une vie
}